# ******************************************************************************
#
#	Global declarations
#
# ******************************************************************************

# ******************************************************************************
# ******************************************************************************
#
#						Functions
#
# ******************************************************************************
# ******************************************************************************

# ******************************************************************************
#
#	ldcDebugResetScreen
#
#		reset the screen to defaults
#
#	parameters:
#		none
#
#	returns:
#		0 => no error
#
# ******************************************************************************
ldcDebugResetScreen()
{
	echo "${ldcclr_NoColor}"
}

# ******************************************************************************
#
#	ldcDebugFuncCommand
#
#		reset the screen to defaults
#
#	parameters:
#		none
#
#	returns:
#		0 => no error
#
# ******************************************************************************
ldcDebugFuncCommand()
{
	trap ldcDebugResetScreen INT
	/bin/bash $FLAGS $SCRIPT
}

